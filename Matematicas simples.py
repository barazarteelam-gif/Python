#Operadores de comparación en python

#1.Igual
n=1==1
z=2==2
print(f'N = {n}')#=True
print(f'Z = {z}')#=False

#2.No igual
m=1!=1
ñ=2!=3
print(f'M = {m}')#=False
print(f'ñ= {ñ}')#=True

#3.Mayor que
s=3>1
t=2>3
print(f'S = {s}')#=True
print(f't = {t}')#=False

#4.Menor que
a=3<14
b=14<13
print(f'A = {a}')#True
print(f'B = {b}')#False

#5.Mayor o igual que
c=33>=14
d=22>=22
e=2>=3
print(f'C = {c}')#True
print(f'D = {d}')#True
print(f'E = {e}')#False

#6.Menor o igual que
f=4<=15
g=14<=14
h=15<=14
print(f'A = {f}')#True
print(f'B = {g}')#True
print(f'B = {h}')#False